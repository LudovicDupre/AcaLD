
//5.1 Methode de Concaténation
public class MéthodeConcatenationLD_5_1 {
	
	public static void main(String[] args) {
		System.out.println("Ludovic" + " Dupre");
		//Résullat attendu : Ludovic Dupré
		// Le symbole '+' à la propriété de concaténer les string ensemble automatiquement.
		System.out.println( 7 + 5 + "chats");
		// Résultat attendu : "75chats"
		System.out.println("FMS".concat(" Academy"));
		// Résultat attendu : "FMS Academy"
		// La méthode '.concat()' peut elle aussi de permettre la concaténation de strings.
	}
}